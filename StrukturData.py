angka8 = np.ones((2,4)) # FUNGSI UNTUK MENGISI DATA 1 DEENGAN MODEL 2 DIMENSI
print(angka8)

# 4. SATU SET ANGKA DENGAN SPASI MERATA DALAM INTERVAL YANG DITENTUKAN

